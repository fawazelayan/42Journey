#include <unistd.h>

void	write_n(int x)
{
	char	dig[2];

	dig[0] = '0' + x / 10 % 10;
	dig[1] = '0' + x % 10;
	write(1, &dig, 2);
}

void	ft_print_comb2(void)
{
	int	n[2];

	n[0] = 0;
	while (n[0] < 99)
	{
		n[1] = n[0] + 1;
		while (n[1] < 100)
		{
			write_n(n[0]);
			write(1, " ", 1);
			write_n(n[1]);
			n[1]++;
			if (n[0] != 98)
				write(1, ", ", 2);
		}
		n[0]++;
	}
}
