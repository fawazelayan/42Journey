#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	unsigned int	i;
	char			*map;

	i = 0;
	if (!s || !f)
		return (NULL);
	map = malloc(ft_strlen(s) + 1);
	if (!map)
		return (NULL);
	while (s[i] != '\0')
	{
		map[i] = f(i, s[i]);
		i++;
	}
	map[i] = '\0';
	return (map);
}
